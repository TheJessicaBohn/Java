public class OperadoresAritmeticos {
	public static void main(String[] args) {

		int a = 10;
		int b = 2;

		System.out.println("a = " + a);
		System.out.println("b = " + b);
		System.out.println("valor a + b = " + (a + b)); // Saída: 12
		System.out.println("valor a - b = " + (a - b)); // Saída: 8
		System.out.println("valor a * b = " + (a * b)); // Saída: 20
		System.out.println("valor a / b = " + (a / b)); // Saída: 5
		System.out.println("valor a % b = " + (a % b)); // Saída: 0

	}
}