public class OperadoresAtribuicao {
	public static void main(String args[]) {

		int numero = 5;

		System.out.println("Valor de numero: " + (numero));
		System.out.println("+= " + (numero+=3));
		System.out.println("-= " + (numero-=2));
		System.out.println("*= " + (numero*=4));
		System.out.println("/= " + (numero/=2));
	}
}