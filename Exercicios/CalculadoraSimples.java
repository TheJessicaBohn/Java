public class CalculadoraSimples {
	public static void main(String args[]){

		var a = 10;
		var b = 2;

		System.out.println("Soma de a + b = "+ (a+b));
		System.out.println("Subitração de a - b = "+ (a-b));
		System.out.println("Multiplicação  de a * b = "+ (a*b));
		System.out.println("Divisão de a / b = "+ (a/b));
		System.out.println("Resto da divisão de a % b = "+ (a%b));
	}
}